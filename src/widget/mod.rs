///
pub type WidgetId = usize;

/// A command to a widget.
#[derive(Debug)]
pub enum WidgetCommand {
    /// Remove the widget.
    Remove,
    /// Gives/removes focus.
    SetHasFocus(bool),
    /// Enables/disables.
    SetIsDisabled(bool),
    /// Hides/shows.
    SetIsHidden(bool),
    /// Sets the given value.
    SetValue(Box<dyn Any>),
}

///
pub enum WidgetError {
    CommandNotHandled(WidgetId, WidgetCommand),
    NoSuchWidget(WidgetId),
}

/// An event generated by a widget.
#[derive(Clone, Debug)]
pub enum WidgetEvent {
    Clicked(WidgetId),
    ValueChanged(WidgetId),
}

///
pub trait Widget {
    ///
    fn apply_size_constraints(&mut self, size_constraints: SizeConstraints) -> Size;

    ///
    fn handle_commands(&mut self, widget_commands: &HashMap<WidgetId, Vec<WidgetCommand>>);

    ///
    fn handle_event(&mut self, event: &UserEvent, widget_events: &mut Vec<WidgetEvent>);

    ///
    fn paint(&self, piet: &mut piet::Piet, region: &Region);

    ///
    fn set_has_focus(&mut self, has_focus: bool);

    ///
    fn set_is_hidden(&mut self, is_hidden: bool);

    ///
    fn set_origin(&mut self, origin: Point);

    ///
    fn widget_id(&self) -> &WidgetId;
}
